    private void directions() {
        if(historia.object != null){
            ArrayList<ArrayList<String>> objactuales = new DadsLastWeek().getListaobjetos();
            int howmanywithminus = 0;

            String[] variosobjetos = historia.object.split(",");
            for (String variosobjeto : variosobjetos) {
                if (variosobjeto.contains("-")) {
                    howmanywithminus++;
                }
            }
            String[] addobjetos = new String[variosobjetos.length - howmanywithminus];
            String[] removeobjetos = new String[howmanywithminus];
            
            if(!isthereagamesaved){
                boolean horadeguardar = false;
                if("D001".equals(new DadsLastWeek().getDia_coddia()) || "D002".equals(new DadsLastWeek().getDia_coddia()) || "D003".equals(new DadsLastWeek().getDia_coddia()) || "D004".equals(new DadsLastWeek().getDia_coddia()) || "D005".equals(new DadsLastWeek().getDia_coddia()) || "D006".equals(new DadsLastWeek().getDia_coddia())){
                    horadeguardar = true;
                }

                
                for (int i = 0; i < removeobjetos.length; i++) {
                    for (int j = i; j < variosobjetos.length; j++) {
                        if (variosobjetos[j].contains("-")) {
                            removeobjetos[i] = variosobjetos[j].replace("-", "");
                            j = variosobjetos.length;
                            if(horadeguardar || "D007".equals(new DadsLastWeek().getDia_coddia())){
                                new Database().insertarOBorrarObjetos(new DadsLastWeek().getIdusuario(), removeobjetos[i], false);
                            }
                        }
                    }
                }

                for (int i = 0; i < addobjetos.length; i++) {
                    for (int j = i; j < variosobjetos.length; j++) {
                        if (!variosobjetos[j].contains("-")) {
                            addobjetos[i] = variosobjetos[j];
                            j = variosobjetos.length;
                            if(horadeguardar || "D007".equals(new DadsLastWeek().getDia_coddia())){
                                new Database().insertarOBorrarObjetos(new DadsLastWeek().getIdusuario(), addobjetos[i], true);
                            }
                        }
                    }
                }
            }

            if (!objactuales.isEmpty()) {
                for (String addobjeto : addobjetos) {
                    objactuales.get(0).add(addobjeto);
                    objactuales.get(1).add(new Database().buscaNomObjeto(addobjeto));
                }
                for (String removeobjeto : removeobjetos) {
                    objactuales.get(0).remove(removeobjeto);
                    objactuales.get(1).remove(new Database().buscaNomObjeto(removeobjeto));
                }
            } else {
                ArrayList<String> objetos = new ArrayList<>();
                ArrayList<String> nomobjetos = new ArrayList<>();
                for (String variosobjeto : variosobjetos) {
                    objetos.add(variosobjeto);
                    nomobjetos.add(new Database().buscaNomObjeto(variosobjeto));
                }
                objactuales.add(objetos);
                objactuales.add(nomobjetos);
            }

            new DadsLastWeek().setListaobjetos(objactuales);
        }
        
    }
    
