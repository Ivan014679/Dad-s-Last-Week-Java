    private void startbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startbuttonActionPerformed
        if (!"A001".equals(new DadsLastWeek().getDecision_iddecision()) && !"A000".equals(new DadsLastWeek().getDecision_iddecision())) {
            dialog = new DLWOptionPane(this, "Est√°s a punto de empezar una partida nueva. Esto borrar√° todos los progresos actuales.<br>¬øSeguro quieres empezar una nueva partida?.", "¬øEmpezar de cero?","question",480,150,true);
            dialog.setVisible(true);
            
//            int option = JOptionPane.showConfirmDialog(null, "Est√°s a punto de empezar una partida nueva. Esto borrar√° todos los progresos actuales.\n¬øSeguro quieres empezar una nueva partida?.", "¬øEmpezar de cero?", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (dialog.hasAccepted()) {
                new Database().actualizarDiaYDecision(new DadsLastWeek().getIdusuario(), "D001", "A001");
                new Database().borrarTodosLosObjetos(new DadsLastWeek().getIdusuario());
                new Database().actualizarPersonaje(new DadsLastWeek().getIdusuario(), new DadsLastWeek().getNompersonaje(), new DadsLastWeek().getApellpersonaje(), 100, "Normal");
                new DadsLastWeek().setDia_coddia("D001");
                new DadsLastWeek().setDecision_iddecision("A001");
                new DadsLastWeek().setListaobjetos(new ArrayList<>());
                new DadsLastWeek().setSalud(100);
                new DadsLastWeek().setEstadosalud("Normal");
                boolean isthereagamesaved = false;
                new Game(isthereagamesaved).setVisible(true);
                this.setVisible(false);
            }
        }else{
            new Database().actualizarDiaYDecision(new DadsLastWeek().getIdusuario(), "D001", "A001");
            new DadsLastWeek().setDia_coddia("D001");
            new DadsLastWeek().setDecision_iddecision("A001");
            new DadsLastWeek().setListaobjetos(new ArrayList<>());
            new DadsLastWeek().setSalud(100);
            new DadsLastWeek().setEstadosalud("Normal");
            boolean isthereagamesaved = false;
            new Game(isthereagamesaved).setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_startbuttonActionPerformed

